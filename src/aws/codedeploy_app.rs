pub struct BuildCodedeployApp {
    pub tf_id: String,
    #[doc= ""]
    pub name: PrimField<String>,
}

impl BuildCodedeployApp {
    pub fn build(self, stack: &mut Stack) -> CodedeployApp {
        let out = CodedeployApp(Rc::new(CodedeployApp_ {
            shared: stack.shared.clone(),
            tf_id: self.tf_id,
            data: RefCell::new(CodedeployAppData {
                depends_on: core::default::Default::default(),
                provider: None,
                lifecycle: core::default::Default::default(),
                for_each: None,
                compute_platform: core::default::Default::default(),
                id: core::default::Default::default(),
                name: self.name,
                tags: core::default::Default::default(),
                tags_all: core::default::Default::default(),
            }),
        }));
        stack.add_resource(out.0.clone());
        out
    }
}

